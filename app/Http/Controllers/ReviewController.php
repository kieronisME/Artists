    public function edit(Review $review)
    {
       if (auth()->user()->id !== $review->user_id && auth()->user()->role !=='admin'){
        return redirect()->route('albums.index')->with('error','Acess DENIED');
       } 

       return view('reviews.edit',compact('review'));
    }

   

