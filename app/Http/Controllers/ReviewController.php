//################################################################################################################################################################################################################################
//                                                                                                Store
//################################################################################################################################################################################################################################
    
    public function store(Request $request, Album $album)
    {

        $request->validate([
            'review' => 'required|string|max:500',
            'rating' => 'required|integer|max:5',

        ]);

        $album->review()->create([
            'user_id' => auth()->id(),
            'review' => 'required|string|max:500',
            'rating' => 'required|integer|max:5',
            'album_id' => $album->id

        ]);

        return to_route('Albums.show', $album)->with('success', 'youjust added a review');

    }



